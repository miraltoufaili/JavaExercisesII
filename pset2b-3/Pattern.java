class Pattern
{
    public static void main(String[] args)
    
    { 


for (int height = 0; height < 5; height++)
{
     for (int j = 0; j <= height; j++)
    {
     System.out.print(" ");
    } 
      for (int k = height; k < 5 ; k++)
       {
       System.out.print("*");
       }
   
    
    
     System.out.println();
}
   
   }
   
}

